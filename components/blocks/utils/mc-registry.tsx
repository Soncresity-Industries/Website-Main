function compareVersions(v1: string, v2: string): number {
  const parts1 = v1.split(".").map(Number);
  const parts2 = v2.split(".").map(Number);

  for (let i = 0; i < Math.max(parts1.length, parts2.length); i++) {
    const part1 = parts1[i] || 0;
    const part2 = parts2[i] || 0;

    if (part1 > part2) return 1;
    if (part1 < part2) return -1;
  }

  return 0;
}

// Registry data structure - single list per type with version info
interface RegistryItem {
  name: string;
  introducedIn: string;
  hiddenSince?: string; // Optional: version from which the item is hidden
  [key: string]: string | undefined; // Support additional dynamic columns
}

interface RegistryColumn {
  key: string;
  label: string;
}

interface RegistryType {
  label: string;
  value: string;
  columns: RegistryColumn[]; // Dynamic columns (excluding "First Introduced In")
  versions: string[]; // in custom order
  items: RegistryItem[];
}

function MCRegistryContent() {
  const router = useRouter();
  const searchParams = useSearchParams();
  const [searchTerm, setSearchTerm] = useState("");
  const [sortColumn, setSortColumn] = useState<string | null>(null);
  const [sortDirection, setSortDirection] = useState<"asc" | "desc">("asc");

  // Get current selections from URL params
  const selectedType = searchParams.get("type") || registryData[0].value;
  const selectedVersion = searchParams.get("version") || "1.21.11";

  // Get the current registry type
  const currentRegistry = useMemo(
    () => registryData.find((r) => r.value === selectedType) || registryData[0],
    [selectedType]
  );

  // Filter items by selected version and search term
  const filteredData = useMemo(
    () => {
      let result = currentRegistry.items
        .filter((item) => compareVersions(item.introducedIn, selectedVersion) <= 0)
        .filter((item) => {
          // If hiddenSince is set and selectedVersion is >= hiddenSince, hide the item
          if (item.hiddenSince && compareVersions(selectedVersion, item.hiddenSince) >= 0) {
            return false;
          }
          return true;
        })
        .filter((item) => item.name.toLowerCase().includes(searchTerm.toLowerCase()));

      // Apply sorting
      if (sortColumn === "introducedIn") {
        result.sort((a, b) => {
          const comparison = compareVersions(a.introducedIn, b.introducedIn);
          if (comparison !== 0) {
            return sortDirection === "asc" ? comparison : -comparison;
          }
          // Secondary sort: alphabetical by name when versions are equal
          return a.name.localeCompare(b.name);
        });
      } else if (sortColumn && sortColumn !== null) {
        // For dynamic columns, do alphabetical sort
        result.sort((a, b) => {
          const valA = (a[sortColumn] || "").toLowerCase();
          const valB = (b[sortColumn] || "").toLowerCase();
          const comparison = valA.localeCompare(valB);
          return sortDirection === "asc" ? comparison : -comparison;
        });
      } else {
        // Default alphabetical sort by name
        result.sort((a, b) => a.name.localeCompare(b.name));
      }

      return result;
    },
    [currentRegistry, selectedVersion, searchTerm, sortColumn, sortDirection]
  );

  // Handle type selection
  const handleTypeChange = useCallback(
    (value: string) => {
      const params = new URLSearchParams(searchParams);
      params.set("type", value);
      // Reset version to the first available version of the new type
      const newRegistry = registryData.find((r) => r.value === value);
      if (newRegistry) {
        params.set("version", newRegistry.versions[0]);
      }
      router.push(`?${params.toString()}`);
    },
    [router, searchParams]
  );

  // Handle version selection
  const handleVersionChange = useCallback(
    (value: string) => {
      const params = new URLSearchParams(searchParams);
      params.set("version", value);
      router.push(`?${params.toString()}`);
    },
    [router, searchParams]
  );

  // Handle column sorting
  const handleSort = useCallback(
    (column: string) => {
      if (sortColumn === column) {
        // If clicking the same column, toggle direction
        setSortDirection(sortDirection === "asc" ? "desc" : "asc");
      } else {
        // If clicking a new column, set it and reset to ascending
        setSortColumn(column);
        setSortDirection("asc");
      }
    },
    [sortColumn, sortDirection]
  );

  // Get sort icon for a column
  const getSortIcon = (column: string) => {
    if (sortColumn !== column) {
      return <ArrowUpDown className="w-4 h-4 ml-2 opacity-50 inline" />;
    }
    return sortDirection === "asc"
      ? <ArrowUp className="w-4 h-4 ml-2 inline" />
      : <ArrowDown className="w-4 h-4 ml-2 inline" />;
  };

  return (
    <Card className="w-full">
      <CardHeader>
        <CardTitle>Minecraft Registry</CardTitle>
        <CardDescription>
          <p>Browse Minecraft registry data</p>
          <p>Note: This registry only goes from 1.14.4 till the newest version of Minecraft as older versions couldn't be decrypted and are mostly not even used anyways</p>
        </CardDescription>
      </CardHeader>
      <CardContent className="space-y-4">
        {/* Dropdowns */}
        <div className="flex gap-4 flex-wrap">
          {/* Type Dropdown */}
          <div className="flex flex-col gap-2">
            <label className="text-sm font-medium">Type of Table</label>
            <Select value={selectedType} onValueChange={handleTypeChange}>
              <SelectTrigger className="w-[200px]">
                <SelectValue />
              </SelectTrigger>
              <SelectContent>
                {registryData.map((type) => (
                  <SelectItem key={type.value} value={type.value}>
                    {type.label}
                  </SelectItem>
                ))}
              </SelectContent>
            </Select>
          </div>

          {/* Version Dropdown */}
          <div className="flex flex-col gap-2">
            <label className="text-sm font-medium">Version</label>
            <Select value={selectedVersion} onValueChange={handleVersionChange}>
              <SelectTrigger className="w-[200px]">
                <SelectValue />
              </SelectTrigger>
              <SelectContent>
                {currentRegistry.versions.map((version) => (
                  <SelectItem key={version} value={version}>
                    {version}
                  </SelectItem>
                ))}
              </SelectContent>
            </Select>
          </div>
        </div>

        {/* Search Input */}
        <div className="flex flex-col gap-2">
          <label className="text-sm font-medium">Search</label>
          <Input
            placeholder="Search entries..."
            value={searchTerm}
            onChange={(e) => setSearchTerm(e.target.value)}
            className={`max-w-sm text-white placeholder:text-white ${
              searchTerm === "" 
                ? "border-white"
                : filteredData.length > 0 
                ? "border-green-500 focus-visible:ring-green-500"
                : "border-red-500 focus-visible:ring-red-500"
            }`}
          />
        </div>

        {/* Table */}
        <div className="border rounded-lg overflow-hidden">
          <Table>
            <TableHeader>
              <TableRow>
                {/* Dynamic columns */}
                {currentRegistry.columns.map((column) => {
                  const columnWidth = `${100 / (currentRegistry.columns.length + 1)}%`;
                  return (
                    <TableHead
                      key={column.key}
                      className="cursor-pointer hover:bg-muted/50 transition-colors select-none"
                      style={{ width: columnWidth }}
                      onClick={() => handleSort(column.key)}
                    >
                      {column.label} {getSortIcon(column.key)}
                    </TableHead>
                  );
                })}
                {/* "First Introduced In" column always last */}
                <TableHead
                  className="cursor-pointer hover:bg-muted/50 transition-colors select-none"
                  style={{ width: `${100 / (currentRegistry.columns.length + 1)}%` }}
                  onClick={() => handleSort("introducedIn")}
                >
                  First Introduced In {getSortIcon("introducedIn")}
                </TableHead>
              </TableRow>
            </TableHeader>
            <TableBody>
              {filteredData.length > 0 ? (
                filteredData.map((item, index) => (
                  <TableRow key={index}>
                    {/* Dynamic column values */}
                    {currentRegistry.columns.map((column) => (
                      <TableCell
                        key={column.key}
                        style={{ width: `${100 / (currentRegistry.columns.length + 1)}%` }}
                      >
                        {item[column.key]}
                      </TableCell>
                    ))}
                    {/* "First Introduced In" value always last */}
                    <TableCell
                      className="text-muted-foreground"
                      style={{ width: `${100 / (currentRegistry.columns.length + 1)}%` }}
                    >
                      {item.introducedIn}
                    </TableCell>
                  </TableRow>
                ))
              ) : (
                <TableRow>
                  <TableCell colSpan={currentRegistry.columns.length + 1} className="text-center text-muted-foreground py-8">
                    No data available for this selection
                  </TableCell>
                </TableRow>
              )}
            </TableBody>
          </Table>
        </div>
      </CardContent>
    </Card>
  );
}

export default function MCRegistry() {
  return (
    <Suspense fallback={<div>Loading...</div>}>
      <MCRegistryContent />
    </Suspense>
  );
}

